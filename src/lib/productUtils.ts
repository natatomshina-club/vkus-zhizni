// Возвращает расширенный список белков из пользовательского ввода.
// Если пользователь ввёл белковый продукт — возвращает его + синонимы.
// Используется для строгой фильтрации рецептов по белку.
export function getInputProteins(userRaw: string[]): string[] {
  const result = new Set<string>()
  for (const product of userRaw) {
    const lower = product.toLowerCase().trim()
    if (PROTEIN_KEYWORDS.has(lower)) {
      result.add(lower)
      const synonyms = PRODUCT_SYNONYMS[lower]
      if (synonyms) synonyms.forEach(s => result.add(s))
    } else {
      for (const key of PROTEIN_KEYWORDS) {
        if (lower.includes(key) || key.includes(lower)) {
          result.add(key)
          const synonyms = PRODUCT_SYNONYMS[key]
          if (synonyms) synonyms.forEach(s => result.add(s))
        }
      }
    }
  }
  return Array.from(result)
}

export function expandProducts(userProducts: string[]): string[] {
  const expanded = new Set<string>()
  for (const product of userProducts) {
    const lower = product.toLowerCase().trim()
    expanded.add(lower)
    if (PRODUCT_SYNONYMS[lower]) {
      PRODUCT_SYNONYMS[lower].forEach(s => expanded.add(s))
    }
    for (const [key, synonyms] of Object.entries(PRODUCT_SYNONYMS)) {
      if (lower.includes(key) || key.includes(lower)) {
        synonyms.forEach(s => expanded.add(s))
      }
    }
    expanded.add(lower.split(' ')[0])
  }
  return Array.from(expanded)
}
