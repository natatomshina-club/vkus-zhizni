/** Возвращает group_key субпродукта по protein_tag, или null если не субпродукт. */
export function getOffalGroup(proteinTag: string | null | undefined): string | null {
  if (!proteinTag) return null
  return OFFAL_GROUP_MAP.get(normalizeRu(proteinTag)) ?? null
}

