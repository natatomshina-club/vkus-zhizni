export function filterVeggies(userProducts: string[]): string[] {
  const result: string[] = []
  for (const p of userProducts) {
    const lower = p.toLowerCase().trim()
    if (!lower) continue
    if (VEGGIE_KEYWORDS.has(lower)) { result.push(lower); continue }
    for (const key of VEGGIE_KEYWORDS) {
      if (lower.includes(key) || key.includes(lower)) { result.push(lower); break }
    }
  }
  return result
}

// Возвращает точные названия белков из пользовательского ввода
// (identity-фильтр по PROTEIN_KEYWORDS через normalizeRu).
// Пользователь выбирает из autocomplete — значения точно соответствуют nutrition.name.
export function getInputProteins(userRaw: string[]): string[] {
  return userRaw.filter(p => PROTEIN_KEYWORDS.has(normalizeRu(p.trim())))
}

export function expandProducts(userProducts: string[]): string[] {
  const expanded = new Set<string>()
  for (const product of userProducts) {
    const lower = product.toLowerCase().trim()
    expanded.add(lower)
    if (PRODUCT_SYNONYMS[lower]) {
      PRODUCT_SYNONYMS[lower].forEach(s => expanded.add(s))
    }
    for (const [key, synonyms] of Object.entries(PRODUCT_SYNONYMS)) {
      if (lower.includes(key) || key.includes(lower)) {
        synonyms.forEach(s => expanded.add(s))
      }
    }
    expanded.add(lower.split(' ')[0])
  }
  return Array.from(expanded)
}
