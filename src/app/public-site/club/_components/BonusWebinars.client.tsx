export default function BonusWebinars() {
  const trackRef = useRef<HTMLDivElement>(null)
  const [atStart, setAtStart] = useState(true)
  const [atEnd, setAtEnd] = useState(false)

  const updateEdges = useCallback(() => {
    const el = trackRef.current
    if (!el) return
    setAtStart(el.scrollLeft <= 1)
    setAtEnd(el.scrollLeft >= el.scrollWidth - el.clientWidth - 1)
  }, [])

  useEffect(() => {
    const el = trackRef.current
    if (!el) return
    updateEdges()
    el.addEventListener('scroll', updateEdges, { passive: true })
    window.addEventListener('resize', updateEdges, { passive: true })
    return () => {
      el.removeEventListener('scroll', updateEdges)
      window.removeEventListener('resize', updateEdges)
    }
  }, [updateEdges])

  const scroll = useCallback((dir: 1 | -1) => {
    const el = trackRef.current
    if (!el) return
    const card = el.querySelector<HTMLElement>('.bw-card')
    const step = card ? card.offsetWidth + 24 : 420
    el.scrollBy({ left: dir * step, behavior: 'smooth' })
  }, [])

  return (
    <section className="bonus-webinars" id="bonus-webinars">
      <div className="bw__inner">
        <span className="section-label">— Бонус —</span>
        <h2 className="section-title bw__heading">
          <span className="bw__heading-accent">БОНУС:</span> библиотека вебинаров<br className="brk-mobile" /> и курсов по женскому здоровью
        </h2>
        <p className="section-intro">
          Авторские вебинары и курсы Натальи — в открытом доступе для каждой участницы клуба.
        </p>

        <div className="bw__carousel-wrap">
          <div className="bw__arrows">
            <button className="bw__arrow" onClick={() => scroll(-1)} disabled={atStart} aria-label="Назад">◀</button>
            <button className="bw__arrow" onClick={() => scroll(1)} disabled={atEnd} aria-label="Вперёд">▶</button>
          </div>
          <div className="bw__track" ref={trackRef}>
            {CARDS.map((card) => (
              <article key={card.title} className="bw-card">
                <span className={`bw-card__badge bw-card__badge--${card.type === 'КУРС' ? 'course' : 'webinar'}`}>
                  {card.type}
                </span>
                <h3 className="bw-card__title">{card.title}</h3>
                <p className="bw-card__hook">{card.hook}</p>
                <p className="bw-card__desc">{card.desc}</p>
                <div className="bw-card__price-block">
                  <span className="bw-card__price-orig">{card.price}</span>
                  <span className="bw-card__price-free">бесплатно для участников клуба</span>
                </div>
              </article>
            ))}
          </div>
        </div>

        <p className="bw__summary">
          Суммарная ценность более <strong>88 000 ₽</strong>. Для участников клуба — <strong>БЕСПЛАТНО.</strong>
        </p>
      </div>
    </section>
  )
}
